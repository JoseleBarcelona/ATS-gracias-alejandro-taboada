package OperadoresAritmeticos.Operadores;

public class OperadoresIncreDecre {
    public static void main(String[] args) {
        int x = 5;
        x++;//x = x + 1; x += 1; Operador que incrementa en 1 el valor. = 6
        x--;//x = x - 1; x -= 1; Operador que decrementa en 1 el valor. = 5
        x += 2; //5 + 2 = 7
        x -= 10; // 7 - 10 = -3

        System.out.println(x);

    }
}
