package Arrays.ArraysArreglos;

public class Array4 {
    public static void main(String[] args) {

        String[] nombres = {"José", "Núria", "Neus", "Papa", "Mama"};

        for (int i=0; i<nombres.length; i++){
            System.out.println(nombres[i]);
        }
    }
}
