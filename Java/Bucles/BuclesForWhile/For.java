package Bucles.BuclesForWhile;
/*

   ciclo for
   for(incicialización; condición; aumento o decremento){
   instrucciones;
   }
 */
public class For {
    public static void main(String[] args) {

        for (int i=0; i<=10; i+=2){ //La variable puede ir dentro del for "int".
            System.out.println(i);
        }

    }
}
