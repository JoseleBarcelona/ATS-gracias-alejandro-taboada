package Bucles.BuclesForWhile;
/*
   Ciclo do While
   do{
   instrucciones;
   }while(condiciones);

 */
public class DoWhile {
    public static void main(String[] args) {
        int i=1;

        do {
            System.out.println(i);
            i++;
        }while (i<=10);
    }
}
